#logical operator
#1. and (If both operands are true the return true otherwise false)
print(True and False)
print(True and True)
print(10 and 3)
print(0 and 3)
print(None and 3)
print(False and 3)

#2. or (If both operands are false then return false otherwise true)
print(False or False)
print(True or False)
print(10 or 3)
print(3 or 10)
print(0 or 3)
print(9 and 0)

#3. not (It will return the opposite of operand)
print(not True)
print(not 10)
print(not '-')

#4.falsy value
print(not 0.0)
print(not '')